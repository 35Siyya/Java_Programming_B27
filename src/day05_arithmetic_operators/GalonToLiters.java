package day05_arithmetic_operators;

public class GalonToLiters {
    public static void main(String[] args) {
        double liters = 3.78541;
        int gallon = 10;
        System.out.println("10 gallons equal to " + (gallon*liters) + "liters");
        gallon = 20;
        System.out.println("20 gallons equal to " + (gallon*liters) + "liters");

    }
}
